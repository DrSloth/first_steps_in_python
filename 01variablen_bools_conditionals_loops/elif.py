# Python also supports the elif clause 
# elif can be used to create an if which may be executed if the previous wasn't

if 2 == 5:
    print("This can't be...")
elif 3 != 3:
    print("Math has forsaken us")
elif 3 == 3:
    # They can be chained like you want
    print("Oh? Everything works?")
else:
    print("Everything is just wrong")
