# Python also includes a way to negate the value of expressions
a = True
b = not a

print(b) # Prints false

# Side Note expressions vs statements:
# A statement is something like `print("Hello, World!")` which has a clear effect.
# Expressions are similiar to statements but also resolve to some value
# There are many kinds of expressions such as literal expressions like `10` or `"Hello, World!"` 
# or expressions involving operators like `10 + 5` or `10 != 5`.
# Every expression evaluates to some kind of type `10 != 5` for instance evaluates to a boolean
