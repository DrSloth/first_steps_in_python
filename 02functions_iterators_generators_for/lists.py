# Lists can be used to store multiple values inside one variable
l = [1,2,3]
print(l) # prints the list value [1, 2, 3]

# append can be used to append values 
l.append(4) # appends the value of 4
print(l) # prints [1, 2, 3, 4]

# pop can be used to get and remove the last value of a list
print(l.pop()) # prints 4

print(l) # prints [1, 2, 3] because 4 was removed
